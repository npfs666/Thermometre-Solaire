void drawHorLine(uint8_t x1, uint8_t x2, uint8_t y) {
    
    uint8_t i;
    for(i=x1; i <= x2; i++) {
        
        setPixel(i, y, 0);
    }
}



void drawVertLine(uint8_t x, uint8_t y1, uint8_t y2) {
    
    uint8_t i;
    for(i=y1; i <= y2; i++) {
        
        setPixel(x, i, 0);
    }
}



void setPixel(uint8_t x, uint8_t y, uint8_t value) {
    
    if( value == 0 ) {
        
        buffer[ x / 8][y] &= clr[x & 7];
    }
    else if( value == 1) {
        
        buffer[ x / 8][y] |= set[x & 7];
    } 
}



void SharpRefresh() {
    
    uint8_t i, k;
    
    SHARP_CS = 1;   // Selection de la puce + tempo associée
    __delay_us(6);

    for( k = 1; k < 97; k++) {

        // Entete
        SPI1BUF = SHARPMEM_BIT_WRITECMD | switchVCOM();
        while(SPI1STATbits.SPITBF);
        
        // Adresse (1-96 encodé sur 8 bits)
        SPI1BUF = BitReverseTable256[k];
        while(SPI1STATbits.SPITBF);
        
        // Data 12 * 8 bits
        for ( i = 0; i < 12; i++ ) {

            SPI1BUF = BitReverseTable256[buffer[i][k - 1]];
            while(SPI1STATbits.SPITBF);
        }
    }

    // Data tranfert period (16bits))
    SPI1BUF = 0;
    while(SPI1STATbits.SPITBF);
    SPI1BUF = 0;
    while(SPI1STATbits.SPITBF);

    __delay_us(2);
    SHARP_CS = 0;
}



uint8_t switchVCOM() {
    
    if( VCOM == SHARPMEM_BIT_VCOM )
        VCOM = 0;
    else
        VCOM = SHARPMEM_BIT_VCOM;
    
    return VCOM;
}



void SharpStaticMode() {
    
    SHARP_CS = 1;   // Selection de la puce + tempo associée
    __delay_us(6);
    
    SPI1BUF = switchVCOM();
    while(SPI1STATbits.SPITBF);
    SPI1BUF = 0x00;
    while(SPI1STATbits.SPITBF);
    
    __delay_us(2);
    SHARP_CS = 0;
}



void SharpClearDisplay() {
    
    uint16_t i,j;
    
    // RAZ (1) du buffer
    for(i = 0; i < 96; i++ ) {
        
        for( j=0; j < 12; j++) {
            buffer[j][i] = 0xFF;
        }
    } 
    
    SHARP_CS = 1;   // Selection de la puce + tempo associée
    __delay_us(6);
    
    SPI1BUF = switchVCOM() | SHARPMEM_BIT_CLEAR;
    while(SPI1STATbits.SPITBF);
    SPI1BUF = 0x00;
    while(SPI1STATbits.SPITBF);
    
    __delay_us(2);
    SHARP_CS = 0;
}


